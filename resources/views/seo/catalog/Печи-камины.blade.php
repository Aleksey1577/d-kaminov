<h2 class="text-2xl font-bold mb-4">Печи-камины — эффективное отопление и уют</h2>

<p>
    Печь-камин — это универсальное оборудование, которое одновременно обогревает помещение и создаёт атмосферу
    живого огня. Такие печи подходят для домов, бань, дач и загородных коттеджей.
</p>

<p>
    В <strong>D-Kaminov</strong> представлено множество моделей: стальные, чугунные, конвекционные и дизайнерские печи.
</p>

<h3 class="text-xl font-semibold mt-6 mb-3">Преимущества печей-каминов</h3>
<ul class="list-disc pl-6 space-y-1">
    <li>Высокая теплоёмкость и КПД</li>
    <li>Красивое панорамное стекло</li>
    <li>Быстрый прогрев помещений</li>
    <li>На выбор — верхнее или заднее подключение дымохода</li>
</ul>

<p class="mt-4">Печь-камин — идеальное решение для загородного дома.</p>
